# three types of data types in R programming
# numerical, character, logical

#numerical 
n = 23.34555555555555555555
print(n)
print(class(n))
n = 23
print(class(n))

#character
c = 'Peaceful'
print(class(c)) 
c = 'V'
print(class(c))

#logical 
l = TRUE
print(l)
print(class(l))
l = FALSE
print(class(l))
print(l)
