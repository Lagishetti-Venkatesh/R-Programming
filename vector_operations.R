
v1 <- c(1, 2, 3, 4, 5)
v2 <- c(0.5, 1.5, 2.5, 3.5, 4.5)

#Sum
v <- v1+v2
print(v)

#sub
v <- v1 - v2
print(v)

#div
v <- v1/v2
print(v)

#mul
v<- v1 * v2
print(v)

#modulus
v<- v1%%v2
print(v)


######## single vector operation

#sum
v <- sum(v1)
print(v)
