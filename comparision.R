# you can apply the arithematic operations directly on vector.
print(my_vector[c(T, T, F)])

my_vector <- c(2, 4, 6, 8, 10)

#greater
print(my_vector[my_vector/2 > 3])

#less than
print(my_vector[my_vector/2 < 3])

#equal to
print(my_vector[my_vector/2 == 3])

#greater than equal to
print(my_vector[my_vector/2 >= 3])

#less than equal to
print(my_vector[my_vector/2 <= 3])

# not equal to
print(my_vector[my_vector/2 != 3])


